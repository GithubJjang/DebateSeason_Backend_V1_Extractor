package com.debateseason.extractor.prompt;

import com.debateseason.extractor.prompt.PromptInput;

import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Getter;

@Getter
@AllArgsConstructor
@Builder
public class PromptBuilder {

	PromptInput promptInput;

	public String getPrompt() {

		String content = promptInput.getTitle();

		return String.format("""
                [역할]
                당신은 뉴스 기사를 분석하여 핵심 키워드를 추출하는 전문가입니다.

                [작업 지시]
                반드시 다음 추출 기준에 따라 뉴스 제목에서 키워드를 추출하시오.

                [단계별 규칙]

                1단계: 대괄호 제거
                - 대괄호([]) 내부 문자열은 메타 정보로 간주하고 제외한다.

                2단계: 고유명사 추출
                - 제목에서 고유명사를 찾으세요 (인명, 지명, 기관명, 단체명)
                - 반드시 제목에 실제로 존재하는 단어만 추출
                - 유사어, 동의어, 추론된 단어 절대 금지
                - 예시: 윤석열, 민주당, 서울, 삼성전자
                - 3단계로 이동 전, 모든 고유명사를 찾으시오

                3단계: 핵심 명사 추출
                - 정치, 경제, 사회, 세계, 과학 분야와 관련된 **명사**만 찾으세요
                - **강조 표현은 절대로 명사가 아니므로, 추출 금지**
                - **동사, 형용사, 부사는 절대 추출 금지**
                - **추상적 개념도 절대 추출 금지**

                **중요 제약 (반드시 준수):**
                1. 정치, 경제, 사회, 세계, 과학 중 하나의 분야에 명확히 속하는 명사만 추출
                2. 분류가 애매하거나 위 5개 분야와 무관한 명사는 절대 추출 금지
                3. 입력된 뉴스 제목에 실제로 존재하는 단어만 추출
                4. 유사어, 동의어, 추론된 단어 생성 절대 금지
                5. 제목에 없는 단어를 임의로 추가하지 말것

                예시 (제목의 맥락에 맞는 관련 명사를 모두 추출):
                - 정치: 선거, 공약, 여야, 탄핵, 결속
                - 경제: 금리, 주가, 부동산, 실업
                - 사회: 저출산, 교육, 범죄, 복지
                - 세계: 전쟁, 외교, 난민, 기후
                - 과학: AI, 백신, 기술, 연구

                4단계: 불필요한 단어 제외
                - 구체적 의미 없이 문법적 역할만 하는 의존명사 제외 (것, 등, 때, 곳, 바, 데, 수, 만, 뿐)
                - 뉴스의 핵심 내용과 무관한 일반적/추상적 단어 제외

                [출력 형식]
                - 반드시 다음 JSON 형식으로 각 단계별 추론 과정을 포함하여 출력하세요(쉼표, 괄호 빠짐 없이):
                {
                  "1단계_대괄호제거후_제목": "처리된 제목",
                  "2단계_고유명사": ["키워드1", "키워드2"],
                  "3단계_핵심명사": ["키워드1", "키워드2"],
                  "4단계_제외된단어": ["키워드1", "키워드2"],
                  "최종결과": {
                    "고유명사": ["키워드1", "키워드2"],
                    "일반명사": ["키워드1", "키워드2"]
                  }
                }

                [입력]
                %s
                """, content);
	}
}
